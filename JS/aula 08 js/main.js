function tabuada (numero){
    let tabuada = 1 
    while (tabuada <= 10){
        let resultado = tabuada*numero
        console.log(`${numero} X ${tabuada} = ${resultado}`)
        tabuada ++;
    }
}
tabuada(2);
 


